SELECT  NUMPROPIEDAD,
        IDTIPO AS TIPO,
        RENTA,
        NVL(GASTOCOMUN,0) AS "GASTO COMUN",
        RENTA * 1.062 AS "NUEVA RENTA",
        NVL(GASTOCOMUN * 1.062, 0) AS "GASTO COMUN REAJUSTADO"
FROM PROPIEDAD;
